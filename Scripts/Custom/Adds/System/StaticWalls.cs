        public static void Initialize()
        {
            //sets specific tiles to los/nowalk.
            foreach (int i in losArray)
            {
                if (TileData.ItemTable[i].Height >= 6) //Taran: Keep this number at 6 or else you can't enter custom houses with most foundations
                {
                    TileData.ItemTable[i].Flags = TileFlag.Wall | TileFlag.NoShoot | TileFlag.Impassable;
                    
                    if (TileData.ItemTable[i].Height < 20) 
                        TileData.ItemTable[i].Height = 20;
                }
            }
        }
